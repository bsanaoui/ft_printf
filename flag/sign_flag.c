#include "libftprintf.h"

char	sign_flag(int width, char c)
{
	if (c == '0')
		return ('0');
	else if (width >= 0 && c != '-')
		return ('+');
	else if (c == '-' || width < 0)
		return ('-');
	return ('+');
}
